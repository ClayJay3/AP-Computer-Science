package Four.Six;

public class Box
{
	// Create instance variables.
	private double height;
	private double width;
	private double depth;
	private boolean full;
	
	public Box(double height, double width, double depth, boolean full)
	{
		this.height = height;
		this.width = width;
		this.depth = depth;
		this.full = full;
	}
}